#include "libft.h"
#include <string.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	c_src;

	i = 0;
	c_src = 0;
	while (src[c_src])
		c_src++;
	if (size < 1)
	{
		return (c_src);
	}
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (c_src);
}
